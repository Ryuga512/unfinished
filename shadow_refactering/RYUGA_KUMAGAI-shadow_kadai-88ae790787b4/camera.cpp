#define	VALUE_MOVE_CAMERA	(2.0f)										// カメラの移動量
#define	VALUE_ROTATE_CAMERA	(D3DX_PI * 0.01f)							// カメラの回転量
#define ZOOM_CAMERA         (30.0f)                                     // ズーム時のカメラ
#define TPS_CAMERA          (100.0f)
